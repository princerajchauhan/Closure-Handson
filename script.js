
// ********************* Question 1 ********************
let code1 = document.getElementById('code1')
let isCode1 = false
function seeHide1(element){
    if (isCode1) {
        element.innerText = "See Code"
        code1.style.display = "none"
        isCode1 = false
    }
    else{
        element.innerText = "Hide Code"
        code1.style.display = "block"
        isCode1 = true
    }
}

